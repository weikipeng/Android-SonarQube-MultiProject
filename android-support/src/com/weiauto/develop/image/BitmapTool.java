    public static void changeImageDrawableColor(ImageView view,int color){
        Bitmap sourceBitmap = BitmapTool.drawableToBitmap(view.getDrawable());
        view.setImageBitmap(changeBitmapColor(sourceBitmap,color));
    }

    public static Bitmap changeBitmapColor(Bitmap bitmap,int color){
        bitmap = bitmap.copy(bitmap.getConfig(), true);
        int height = bitmap.getHeight();
        int width = bitmap.getWidth();
        int[] pixels = new int[height * width];
        bitmap.getPixels(pixels,0,width,0,0,width,height);
        for(int i=0;i<(width*height);i++){
            if(pixels[i]!= Color.TRANSPARENT){
                pixels[i] = color;
            }
        }

        bitmap.setPixels(pixels,0,width,0,0,width,height);
        return bitmap;
    }

    public static void changeTextviewDrawableColor(TextView view,int color){
        if(view == null){
            return;
        }
        Drawable[] drawables = view.getCompoundDrawables();
        Bitmap sourceBitmap = null;
        int size = drawables.length;
        Drawable drawable = null;
        Resources resources = view.getResources();
        for(int i=0;i<size;i++){
            drawable = drawables[i];
            if(drawable!=null){
                sourceBitmap = drawableToBitmap(drawable);
                sourceBitmap = changeBitmapColor(sourceBitmap,color);
                drawables[i] = new BitmapDrawable(resources,sourceBitmap);
            }
        }

        view.setCompoundDrawablesWithIntrinsicBounds(drawables[0],drawables[1],drawables[2],drawables[3]);
    }

