	public static Bitmap getScaledFitBitmap(Context context,int mSourceId,int mDstWidth,int mDstHeight){
        // Part 1: Decode image
        Bitmap unscaledBitmap = decodeResource(context.getResources(), mSourceId,
                mDstWidth, mDstHeight, ScalingLogic.FIT);

        // Part 2: Scale image
        Bitmap scaledBitmap = createScaledBitmap(unscaledBitmap, mDstWidth,
                mDstHeight, ScalingLogic.FIT);
        unscaledBitmap.recycle();
        
        return scaledBitmap;
	}
	
	
